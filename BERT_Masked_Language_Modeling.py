        "def predict_mask(sentence):\n",
        "\n",
        "    # Check whether sentence contains [MASK]\n",
        "    if tokenizer.mask_token not in sentence:\n",
        "        raise ValueError(\n",
        "            f\"Sentence must contain {tokenizer.mask_token}\"\n",
        "        )\n",
        "\n",
        "    # Tokenize\n",
        "    inputs = tokenizer(sentence, return_tensors=\"pt\")\n",
        "\n",
        "    input_ids = inputs[\"input_ids\"]\n",
        "    attention_mask = inputs[\"attention_mask\"]\n",
        "\n",
        "    # Forward pass\n",
        "    with torch.no_grad():\n",
        "        outputs = model(\n",
        "            input_ids=input_ids,\n",
        "            attention_mask=attention_mask\n",
        "        )\n",
        "\n",
        "    logits = outputs.logits\n",
        "\n",
        "    # Find MASK position\n",
        "    mask_index = torch.where(\n",
        "        input_ids == tokenizer.mask_token_id\n",
        "    )[1]\n",
        "\n",
        "    # Predict token\n",
        "    predicted_token_id = torch.argmax(\n",
        "        logits[0, mask_index],\n",
        "        dim=-1\n",
        "    )\n",
        "\n",
        "    predicted_word = tokenizer.convert_ids_to_tokens(\n",
        "        predicted_token_id.item()\n",
        "    )\n",
        "\n",
        "    predicted_word = predicted_word.replace(\"##\", \"\")\n",
        "\n",
        "    # Replace MASK token\n",
        "    input_ids[0, mask_index] = predicted_token_id\n",
        "\n",
        "    completed_sentence = tokenizer.decode(\n",
        "        input_ids[0],\n",
        "        skip_special_tokens=True\n",
        "    )\n",
        "\n",
        "    return predicted_word, completed_sentence\n"
